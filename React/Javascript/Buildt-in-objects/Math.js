// Math Constants
console.log(Math.PI); // Output: 3.141592653589793
console.log(Math.E); // Output: 2.718281828459045
console.log(Math.SQRT2); // Output: 1.4142135623730951

// Math Methods
console.log(Math.abs(-5)); // Output: 5 (absolute value)
console.log(Math.ceil(4.3)); // Output: 5 (round up)
console.log(Math.floor(4.9)); // Output: 4 (round down)
console.log(Math.round(4.5)); // Output: 5 (round to nearest integer)
console.log(Math.max(1, 5, 10)); // Output: 10 (maximum value)
console.log(Math.min(1, 5, 10)); // Output: 1 (minimum value)
console.log(Math.pow(2, 3)); // Output: 8 (exponentiation)
console.log(Math.sqrt(16)); // Output: 4 (square root)
console.log(Math.random()); // Output: Random floating-point number between 0 and 1

// Trigonometric Methods (in radians)
console.log(Math.sin(Math.PI / 2)); // Output: 1 (sine)
console.log(Math.cos(0)); // Output: 1 (cosine)
console.log(Math.tan(Math.PI / 4)); // Output: 1 (tangent)
console.log(Math.atan(1)); // Output: 0.7853981633974483 (arctangent)
